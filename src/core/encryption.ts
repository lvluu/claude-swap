import { createCipheriv, createDecipheriv, randomBytes, pbkdf2Sync, createHash } from "crypto";
import { existsSync, mkdirSync, readFileSync, writeFileSync } from "fs";
import { homedir, hostname, platform, arch, cpus } from "os";

const ALGORITHM = "aes-256-gcm";
const KEY_LEN = 32;
const IV_LEN = 16;
const AUTH_TAG_LEN = 16;
const ITERATIONS = 100_000;
const DIGEST = "sha512";

export const CCS_CONFIG_DIR = `${homedir()}/.config/ccs`;

export class EncryptionError extends Error {
  constructor(message: string) {
    super(message);
    this.name = "EncryptionError";
  }
}

/**
 * Returns the path to the machine-id file.
 * Does NOT create the file — caller should call ensureMachineIdFile() first.
 */
export function getMachineIdPath(): string {
  return `${CCS_CONFIG_DIR}/machine-id`;
}

